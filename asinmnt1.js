//Create a basic calculator program using javascript

let choise = 'c';

switch(choise) {
    case 'a':
        console.log(5 + 5);
        break;
    case 'b':
        console.log(20 - 5);
        break;
    case 'c':
        console.log(15 * 5);
        break;
    case 'd':
        console.log(30 / 3);
        break;
    default:
        console.log("Invalid Choise");
}